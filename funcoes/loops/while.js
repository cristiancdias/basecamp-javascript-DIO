//While
//Execulta loop enquanto condição for verdadeira
function exemploWhile() {
  let num = 0
  while (num <= 5) {
    console.log(num)
    num++
  }
}
exemploWhile()

//DoWhile
//Execulta loop pelomenos uma vez e faz verificação.
function exemploDoWhile() {
  let num = 6
  do {
    console.log(num)
    num++
  } while (num <= 5)
}

exemploDoWhile()
